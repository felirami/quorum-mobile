// Lazily-built inverted index: keyword substring → Set<emoji>
let invertedIndex: Map<string, Set<string>> | null = null;

function buildIndex(): Map<string, Set<string>> {
  const index = new Map<string, Set<string>>();
  for (const [emoji, keywords] of Object.entries(EMOJI_KEYWORDS)) {
    for (const kw of keywords) {
      // Index each keyword and all its prefixes (min 2 chars)
      for (let len = 2; len <= kw.length; len++) {
        const prefix = kw.slice(0, len);
        let set = index.get(prefix);
        if (!set) {
          set = new Set();
          index.set(prefix, set);
        }
        set.add(emoji);
      }
    }
  }
  return index;
}

function getIndex(): Map<string, Set<string>> {
  if (!invertedIndex) {
    invertedIndex = buildIndex();
  }
  return invertedIndex;
}

/**
 * Search emojis by keyword using inverted index.
 * Falls back to substring match for queries that don't hit the index.
 */
export function searchEmojis(query: string, emojis: string[]): string[] {
  if (!query) return emojis;
  const lowerQuery = query.toLowerCase();

  // Use inverted index for fast lookup
  const index = getIndex();
  const indexed = index.get(lowerQuery);

  if (indexed) {
    // Filter to only emojis in the provided set
    return emojis.filter((emoji) => indexed.has(emoji));
  }

  // Fallback: substring match on keywords for longer/non-prefix queries
  return emojis.filter((emoji) => {
    const keywords = EMOJI_KEYWORDS[emoji];
    if (!keywords) return false;
    return keywords.some((kw) => kw.includes(lowerQuery));
  });
}
